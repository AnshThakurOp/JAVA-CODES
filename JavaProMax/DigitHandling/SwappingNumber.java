package JavaProMax.DigitHandling;

public class SwappingNumber {
    public static void main(String[] args) {

        // Program to swap to numbers in java : =>

        int num1 = 10;
        int num2 = 20;

        int temp = num1; // Creating a temp variable to store the swapping value temporary.
        num1 = num2;
        num2 = temp;
        System.out.println(num1);
        System.out.println(num2);

    }

}
