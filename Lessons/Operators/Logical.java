package Lessons.Operators;

public class Logical {
    public static void main(String[] args) {
        
/*
    * Logical Operators : 

    * 1: (&& - and)  - returns true if both values are true
    * 2: (|| - or) - returns true if any value is true
    * 3: (! - not) - returns true if value is false, otherwise returns false i.e, when the value is true

    * Examples : 
 */
    System.out.println((true && false)); //The output will be false 
    System.out.println((true|true));
    System.out.println(!true);
    }
    
}
