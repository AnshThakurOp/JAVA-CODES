package Lessons.StringFunctions;

public class IndexOf {
    public static void main(String[] args) {

/*
 * indexOf() - returns the index value of the given char.
 * returns -1 if the char doesn't exists.
 */

    String name = "Ansh";
    System.out.println(name.indexOf('k')); // Returns -1 as k is not present in the name
    System.out.println(name.indexOf('h'));


    }
    
}
