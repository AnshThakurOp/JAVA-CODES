package Lessons;

public class EscapeSequences {
    public static void main(String[] args) {

        // 1 - ( "\"String\"" ) / use forwardslash to print double quotes in the string
        // ; "\" -- Forward slash
        System.out.println("\"Ansh Thakur\"");

        // 2 - For printing backslash in the string we have to use backslash at the
        // ending of the string
        // in the double quotes.
        System.out.println("This will print backslash at the ending /");

        // 3 - For spliting strings in new line we use "\n" escape sequence inside the
        // string from where we have
        // split the string
        System.out.println("Ansh \nThakur");
    }
}
